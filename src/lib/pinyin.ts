/**
 * 将中文文本转为拼音小写，不认识的字原样保留
 */
export function toPinyin(text: string): string {
  if (!text) return '';
  const chars = text.split('');
  let result = '';
  for (const ch of chars) {
    if (ch in PINYIN_MAP) {
      result += PINYIN_MAP[ch];
    } else if (/[a-zA-Z0-9]/.test(ch)) {
      result += ch.toLowerCase();
    } else if (ch === ' ') {
      result += '_';
    } else {
      result += ch;
    }
  }
  return result;
}